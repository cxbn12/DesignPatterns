package abstract_factory_mode.model;

/**
 * @description: 部门表
 * @author: wuyanbo
 * @create: 2019-04-20 21:22
 **/

public class Department {

    private String id;

    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Department{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                '}';
    }
}
